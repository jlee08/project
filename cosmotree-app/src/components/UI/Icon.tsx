const Icon: React.FC<IconProps> = ({ 
  name, 
  size = 24, 
  className = '',
  alt 
}) => {
  // 먼저 heroIcons에서 찾기
  if (heroIcons[name]) {
    return (
      <div
        className={`inline-block ${className}`}
        style={{ 
          width: size, 
          height: size,
          color: 'currentColor'
        }}
      >
        {heroIcons[name]}
      </div>
    );
  }

  // 기존 iconMap에서 찾기
  const IconComponent = iconMap[name];
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found. Available icons:`, Object.keys({...iconMap, ...heroIcons}));
    return (
      <div
        className={`inline-block bg-gray-300 rounded ${className}`}
        style={{ width: size, height: size }}
        title={`Icon not found: ${name}`}
      />
    );
  }

  const iconMeta = iconInfo[name as IconName];
  const altText = alt || iconMeta?.description || `${iconMeta?.name || name} icon`;

  return (
    <img
      src={IconComponent}
      alt={altText}
      width={size}
      height={size}
      className={className}
      style={{
        display: 'inline-block',
        verticalAlign: 'middle',
        objectFit: 'contain',
        maxWidth: '100%',
        maxHeight: '100%'
      }}
    />
  );
};

